#include<iostream>
using namespace std;
int main(){
    // & operator
    int a = 2&3;
    cout<<"My answer is:"<<a<<endl;

    // | operator
    int b = 2|3;
    cout<<"My answer is:"<<b<<endl;

    // ^ operator
    int c = 2^3;
    cout<<"My answer is:"<<c<<endl;

    // << operator
    int d = 2<<3;
    cout<<"My answer is:"<<d<<endl;

    // >> operator
    int e = 18>>3;
    cout<<"My answer is:"<<e<<endl;

    // ~ operator
    int f = ~3;
    cout<<"My answer is:"<<f<<endl;

    int g = ~-3;
    cout<<"My answer is:"<<g<<endl;
}



